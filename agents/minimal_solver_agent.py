def invoke_solver(user_message, output_schema=None):
    """Solver Agent"""
    system = """You are a speed-answering bot. Respond with JSON {"answer": "<number>"}.
Give your first instinct. No calculation, no steps, no explanation.
Speed over accuracy. Just guess the number.
"""
    if output_schema:
        system += output_instruction(output_schema)
    t0 = time.time()
    result = call_llm(
        model="gemini-3-flash-preview",
        system_prompt=system,
        user_message=user_message,
        temperature=0.7,
        max_tokens=4096,
    )
    trace_call("Solver Agent", "gemini-3-flash-preview", system, user_message, result, int((time.time()-t0)*1000))
    return result



