class AgentState:
    """Runtime state for Knowledge Graph RAG"""
    def __init__(self):
        self.knowledge_graph = Store_knowledge_graph()
        self.data = {}  # current data flowing through the pipeline
        self.iteration = 0
        self.schema_violations = 0


