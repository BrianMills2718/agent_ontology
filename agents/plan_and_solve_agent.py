def process_receive_problem(state):
    """
    Receive Problem
    Accept the complex problem and initialize state
    """
    print(f"  → Receive Problem")

    # Logic from spec
    state.data["solved_sub_solutions"] = []
    state.data["current_sub_index"] = 0
    state.data["retry_count"] = 0
    state.data["max_retries"] = 2
    print(f"    Problem: {state.data.get('problem', '')[:100]}")
    if state.data.get("_done"):
        return state

    return state


def process_decompose(state):
    """
    Decompose Problem
    Invoke the planner agent to break the problem into sub-problems
    """
    print(f"  → Decompose Problem")

    # Logic from spec
    print(f"    Decomposing problem into sub-problems...")
    if state.data.get("_done"):
        return state

    # Flatten nested dicts: promote schema fields to top-level state.data
    for _nested_val in list(state.data.values()):
        if isinstance(_nested_val, dict):
            for _nk, _nv in _nested_val.items():
                if _nk not in state.data:
                    state.data[_nk] = _nv

    # Invoke: Decompose into sub-problems
    planner_input = build_input(state, "PlannerInput")
    planner_msg = json.dumps(planner_input, default=str)
    planner_raw = invoke_planner(planner_msg, output_schema="Decomposition")
    planner_result = parse_response(planner_raw, "Decomposition")
    # Validate and merge output fields into state.data
    state.schema_violations += len(validate_output(planner_result, "Decomposition"))
    state.data.update(planner_result)
    state.data["decomposition"] = planner_result
    state.data["decompose_result"] = planner_result
    print(f"    ← Planner Agent: {planner_result}")

    return state


def process_solve_subproblem(state):
    """
    Solve Sub-problem
    Invoke the solver agent on the current sub-problem
    """
    print(f"  → Solve Sub-problem")

    # Logic from spec
    sub_problems = state.data.get("sub_problems", [])
    idx = state.data.get("current_sub_index", 0)
    if idx < len(sub_problems):
        current = sub_problems[idx]
        state.data["current_sub_description"] = current.get("description", "")
        print(f"    Solving sub-problem {idx + 1}/{len(sub_problems)}: {current.get('description', '')[:80]}")
    else:
        print(f"    No more sub-problems to solve")
    if state.data.get("_done"):
        return state

    # Flatten nested dicts: promote schema fields to top-level state.data
    for _nested_val in list(state.data.values()):
        if isinstance(_nested_val, dict):
            for _nk, _nv in _nested_val.items():
                if _nk not in state.data:
                    state.data[_nk] = _nv

    # Invoke: Solve current sub-problem
    solver_input = build_input(state, "SolverInput")
    solver_msg = json.dumps(solver_input, default=str)
    solver_raw = invoke_solver(solver_msg, output_schema="SubSolution")
    solver_result = parse_response(solver_raw, "SubSolution")
    # Validate and merge output fields into state.data
    state.schema_violations += len(validate_output(solver_result, "SubSolution"))
    state.data.update(solver_result)
    state.data["sub_solution"] = solver_result
    state.data["solve_subproblem_result"] = solver_result
    print(f"    ← Solver Agent: {solver_result}")

    return state


def process_verify_solution(state):
    """
    Solution valid?
    """
    print(f"  → Solution valid?")

    # Gate: is_valid == True
    # Branch: valid → accept_solution
    # Branch: invalid → retry_or_next

    if state.data.get("is_valid") == True:
        print(f"    → valid")
        return "accept_solution"
    else:
        print(f"    → invalid")
        return "retry_or_next"


def process_accept_solution(state):
    """
    Accept Solution
    Record the verified sub-solution and advance to the next sub-problem
    """
    print(f"  → Accept Solution")

    # Logic from spec
    solution = {
        "index": state.data.get("current_sub_index", 0),
        "description": state.data.get("current_sub_description", ""),
        "solution_text": state.data.get("solution_text", ""),
        "confidence": state.data.get("confidence", 0),
    }
    solved = state.data.get("solved_sub_solutions", [])
    solved.append(solution)
    state.data["solved_sub_solutions"] = solved
    state.data["current_sub_index"] = state.data.get("current_sub_index", 0) + 1
    state.data["retry_count"] = 0
    print(f"    Accepted solution for sub-problem {solution['index'] + 1}")
    if state.data.get("_done"):
        return state

    return state


def process_retry_or_next(state):
    """
    Retries left?
    """
    print(f"  → Retries left?")

    # Gate: retry_count < max_retries
    # Branch: retries remaining → solve_subproblem
    # Branch: retries exhausted → accept_solution

    if (state.data.get("retry_count", 0)) < (state.data.get("max_retries", 0)):
        print(f"    → retries remaining")
        return "solve_subproblem"
    else:
        print(f"    → retries exhausted")
        return "accept_solution"


def process_check_remaining(state):
    """
    More sub-problems?
    """
    print(f"  → More sub-problems?")

    # Gate: current_sub_index < total_sub_problems
    # Branch: more sub-problems → solve_subproblem
    # Branch: all solved → synthesize

    if (state.data.get("current_sub_index", 0)) < (state.data.get("total_sub_problems", 0)):
        print(f"    → more sub-problems")
        return "solve_subproblem"
    else:
        print(f"    → all solved")
        return "synthesize"


def process_invoke_verifier(state):
    """
    Invoke Verifier
    Send the current sub-solution to the verifier agent for checking
    """
    print(f"  → Invoke Verifier")

    # Logic from spec
    state.data["retry_count"] = state.data.get("retry_count", 0) + 1
    print(f"    Verifying sub-problem {state.data.get('current_sub_index', 0) + 1} (attempt {state.data['retry_count']})")
