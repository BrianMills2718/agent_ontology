PROCESSES = {
    "receive_problem": process_receive_problem,
    "decompose": process_decompose,
    "solve_subproblem": process_solve_subproblem,
    "verify_solution": process_verify_solution,
    "accept_solution": process_accept_solution,
    "retry_or_next": process_retry_or_next,
    "check_remaining": process_check_remaining,
    "invoke_verifier": process_invoke_verifier,
    "synthesize": process_synthesize,
}

TRANSITIONS = {
    "receive_problem": "decompose",
    "decompose": "solve_subproblem",
    "solve_subproblem": "invoke_verifier",
    # "verify_solution": determined by gate logic
    "accept_solution": "check_remaining",
    # "retry_or_next": determined by gate logic
    # "check_remaining": determined by gate logic
    "invoke_verifier": "verify_solution",
    "synthesize": None,  # terminal
}


MAX_ITERATIONS = int(os.environ.get("AGENT_ONTOLOGY_MAX_ITER", "100"))


def run(initial_data=None):
    """Plan and Solve — main execution loop"""
    state = AgentState()
    if initial_data:
        state.data.update(initial_data)

    current = "receive_problem"
    print(f"\n════════════════════════════════════════════════════════════")
    print(f"  Plan and Solve")
    print(f"  Plan-and-Solve agent that decomposes a problem into sub-problems, solves each with verification and retry logic, then synthesizes a final answer.")
    print(f"════════════════════════════════════════════════════════════\n")

    while current and state.iteration < MAX_ITERATIONS:
        state.iteration += 1
        print(f"\n[Iteration {state.iteration}] State: {current}")

        process_fn = PROCESSES.get(current)
        if not process_fn:
            print(f"  Unknown process: {current}")
            break

        result = process_fn(state)

        if current in ['verify_solution', 'retry_or_next', 'check_remaining']:
            current = result
        else:
            current = TRANSITIONS.get(current)

        # Fan-out: if transition is a list, run all branches sequentially
        while isinstance(current, list):
            _targets = current
            for _ft in _targets:
                state.iteration += 1
                print(f"\n[Iteration {state.iteration}] State: {_ft} (fan-out)")
                _fn = PROCESSES.get(_ft)
                if _fn:
                    _fn(state)
            # Collect unique next-hops from all fan-out targets
            _next_set = []
            for _ft in _targets:
                _nt = TRANSITIONS.get(_ft)
                if _nt is not None and _nt not in _next_set:
                    _next_set.append(_nt)
            current = _next_set[0] if len(_next_set) == 1 else (_next_set if _next_set else None)

        if current is None or state.data.get("_done"):
            print("\n  [DONE] Reached terminal state.")
            break

    _clean_exit = state.iteration < MAX_ITERATIONS
    if state.iteration >= MAX_ITERATIONS:
        print(f"\n  [STOPPED] Max iterations ({MAX_ITERATIONS}) reached.")
        _clean_exit = False

    if state.schema_violations > 0:
        print(f"\n  [SCHEMA] {state.schema_violations} total schema violation(s) during execution")
    dump_trace(iterations=state.iteration, clean_exit=_clean_exit)
    print(f"\nFinal state.data keys: {list(state.data.keys())}")
    return state


if __name__ == "__main__":
    run()