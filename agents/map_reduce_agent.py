def process_shuffle_results(state):
    """
    Shuffle and Group
    Group and deduplicate findings from parallel map tasks.
    """
    print(f"  → Shuffle and Group")

    # Logic from spec
    # Collect results from the 'map_chunks' spawn process
    # Results are typically found in state.data['chunk_result'] as a list
    raw_results = state.data.get("chunk_result", [])
    
    # Deduplication logic based on content similarity or offsets
    deduplicated = []
    seen_findings = set()
    for res in raw_results:
        for finding in res.get("findings", []):
            if finding not in seen_findings:
                deduplicated.append(finding)
                seen_findings.add(finding)
    
    state.data["deduplicated_results"] = deduplicated
    print(f"Shuffle complete. {len(deduplicated)} unique findings identified.")
    if state.data.get("_done"):
        return state

    return state


def process_reduce_results(state):
    """
    Reduce Results
    Prepare deduplicated data and invoke the Reduce Agent for synthesis.
    """
    print(f"  → Reduce Results")

    # Logic from spec
    state.data["retry_count"] = state.data.get("retry_count", 0) + 1
    # Prepare the input for the reduce agent
    state.data["reduce_input"] = {
        "task_description": state.data.get("task_description"),
        "findings": state.data.get("deduplicated_results", [])
    }
    if state.data.get("_done"):
        return state

    # Flatten nested dicts: promote schema fields to top-level state.data
    for _nested_val in list(state.data.values()):
        if isinstance(_nested_val, dict):
            for _nk, _nv in _nested_val.items():
                if _nk not in state.data:
                    state.data[_nk] = _nv

    # Invoke: Synthesize Findings
    reduce_agent_input = build_input(state, "ReduceTaskInput")
    reduce_agent_msg = json.dumps(reduce_agent_input, default=str)
    reduce_agent_raw = invoke_reduce_agent(reduce_agent_msg, output_schema="ReduceOutputSchema")
    reduce_agent_result = parse_response(reduce_agent_raw, "ReduceOutputSchema")
    # Validate and merge output fields into state.data
    state.schema_violations += len(validate_output(reduce_agent_result, "ReduceOutputSchema"))
    state.data.update(reduce_agent_result)
    state.data["reduce_output_schema"] = reduce_agent_result
    state.data["reduce_results_result"] = reduce_agent_result
    print(f"    ← Reduce Agent: {reduce_agent_result}")

    return state


