def process_chunk_document(state):
    """
    Chunk Document
    Split the input document into overlapping chunks based on token size.
    """
    print(f"  → Chunk Document")

    # Logic from spec
    text = state.data.get("full_text", "")
    chunk_size = state.data.get("chunk_size", 1000)
    overlap = int(chunk_size * 0.1)
    
    # Mock chunking logic
    chunks = []
    words = text.split()
    for i in range(0, len(words), chunk_size - overlap):
        chunk_text = " ".join(words[i:i + chunk_size])
        chunks.append({
            "chunk_id": f"chunk_{len(chunks)}",
            "content": chunk_text,
            "start_offset": i,
            "end_offset": i + len(chunk_text)
        })
    
    state.data["chunks"] = chunks
    state.data["chunk_count"] = len(chunks)
    state.data["retry_count"] = 0
    print(f"Document split into {len(chunks)} chunks.")
    if state.data.get("_done"):
        return state

    return state


