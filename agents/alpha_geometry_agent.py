def process_receive_problem(state):
    """
    Receive Geometry Problem
    """
    print(f"  → Receive Geometry Problem")

    # Logic from spec
    state.data["problem"] = state.data.get("problem", state.data.get("query", ""))
    state.data["goal"] = state.data.get("goal", "Prove triangle ABC is isosceles")
    state.data["known_facts"] = state.data.get("known_facts", ["AB = AC", "angle BAC = 60"])
    state.data["iteration"] = 0
    state.data["max_iterations"] = state.data.get("max_iterations", 5)
    state.data["proof_steps"] = []
    if state.data.get("_done"):
        return state

    return state


def process_propose_construction(state):
    """
    Propose Construction
    """
    print(f"  → Propose Construction")

    # Invoke: construction_proposer
    construction_proposer_input = build_input(state, "ConstructionInput")
    construction_proposer_msg = json.dumps(construction_proposer_input, default=str)
    construction_proposer_raw = invoke_construction_proposer(construction_proposer_msg, output_schema="ConstructionOutput")
    construction_proposer_result = parse_response(construction_proposer_raw, "ConstructionOutput")
    # Validate and merge output fields into state.data
    state.schema_violations += len(validate_output(construction_proposer_result, "ConstructionOutput"))
    state.data.update(construction_proposer_result)
    state.data["construction_output"] = construction_proposer_result
    state.data["propose_construction_result"] = construction_proposer_result
    print(f"    ← Construction Proposer (LLM): {construction_proposer_result}")

    return state


