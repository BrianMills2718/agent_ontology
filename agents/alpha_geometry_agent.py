PROCESSES = {
    "receive_problem": process_receive_problem,
    "propose_construction": process_propose_construction,
    "translate_to_formal": process_translate_to_formal,
    "run_deduction": process_run_deduction,
    "check_proved": process_check_proved,
    "check_iterations": process_check_iterations,
    "update_facts": process_update_facts,
    "emit_proof": process_emit_proof,
    "emit_failure": process_emit_failure,
    "handle_timeout": process_handle_timeout,
}

TRANSITIONS = {
    "receive_problem": "propose_construction",
    "propose_construction": "translate_to_formal",
    "translate_to_formal": "run_deduction",
    "run_deduction": "update_facts",
    # "check_proved": determined by gate logic
    # "check_iterations": determined by gate logic
    "update_facts": "check_proved",
    "emit_proof": None,  # terminal
    "emit_failure": None,  # terminal
    "handle_timeout": "update_facts",
}


MAX_ITERATIONS = int(os.environ.get("AGENT_ONTOLOGY_MAX_ITER", "100"))


def run(initial_data=None):
    """AlphaGeometry-style Agent — main execution loop"""
    state = AgentState()
    if initial_data:
        state.data.update(initial_data)

    current = "receive_problem"
    print(f"\n════════════════════════════════════════════════════════════")
    print(f"  AlphaGeometry-style Agent")
    print(f"  Neural-symbolic geometry solver inspired by AlphaGeometry (Trinh et al. 2024). LLM proposes construction steps; symbolic deduction engine checks validity and derives consequences. The loop continues until goal is proved or max iterations reached.")
    print(f"════════════════════════════════════════════════════════════\n")

    while current and state.iteration < MAX_ITERATIONS:
        state.iteration += 1
        print(f"\n[Iteration {state.iteration}] State: {current}")

        process_fn = PROCESSES.get(current)
        if not process_fn:
            print(f"  Unknown process: {current}")
            break

        result = process_fn(state)

        if current in ['check_proved', 'check_iterations']:
            current = result
        else:
            current = TRANSITIONS.get(current)

        # Fan-out: if transition is a list, run all branches sequentially
        while isinstance(current, list):
            _targets = current
            for _ft in _targets:
                state.iteration += 1
                print(f"\n[Iteration {state.iteration}] State: {_ft} (fan-out)")
                _fn = PROCESSES.get(_ft)
                if _fn:
                    _fn(state)
            # Collect unique next-hops from all fan-out targets
            _next_set = []
            for _ft in _targets:
                _nt = TRANSITIONS.get(_ft)
                if _nt is not None and _nt not in _next_set:
                    _next_set.append(_nt)
            current = _next_set[0] if len(_next_set) == 1 else (_next_set if _next_set else None)

        if current is None or state.data.get("_done"):
            print("\n  [DONE] Reached terminal state.")
            break

    _clean_exit = state.iteration < MAX_ITERATIONS
    if state.iteration >= MAX_ITERATIONS:
        print(f"\n  [STOPPED] Max iterations ({MAX_ITERATIONS}) reached.")
        _clean_exit = False

    if state.schema_violations > 0:
        print(f"\n  [SCHEMA] {state.schema_violations} total schema violation(s) during execution")
    dump_trace(iterations=state.iteration, clean_exit=_clean_exit)
    print(f"\nFinal state.data keys: {list(state.data.keys())}")
    return state


if __name__ == "__main__":
    run()