def process_check_proved(state):
    """
    Goal Proved?
    """
    print(f"  → Goal Proved?")

    # Gate: goal_proved
    # Branch: goal_proved is true → emit_proof
    # Branch: goal_proved is false → check_iterations

    if bool(state.data.get("goal_proved")):
        print(f"    → goal_proved is true")
        return "emit_proof"
    else:
        print(f"    → goal_proved is false")
        return "check_iterations"


def process_check_iterations(state):
    """
    Iterations Remaining?
    """
    print(f"  → Iterations Remaining?")

    # Gate: iteration < max_iterations
    # Branch: iteration < max_iterations → propose_construction
    # Branch: iteration >= max_iterations → emit_failure

    if (state.data.get("iteration", 0)) < (state.data.get("max_iterations", 0)):
        print(f"    → iteration < max_iterations")
        return "propose_construction"
    else:
        print(f"    → iteration >= max_iterations")
        return "emit_failure"


def process_update_facts(state):
    """
    Update Known Facts
    """
    print(f"  → Update Known Facts")

    # Logic from spec
    new_facts = state.data.get("new_facts", [])
    state.data["known_facts"] = state.data.get("known_facts", []) + new_facts
    state.data["iteration"] = state.data.get("iteration", 0) + 1
    construction = state.data.get("construction", "")
    if construction:
        state.data["proof_steps"] = state.data.get("proof_steps", []) + [construction]
    if state.data.get("_done"):
        return state

    return state


def process_emit_proof(state):
    """
    Emit Proof
    """
    print(f"  → Emit Proof")

    # Logic from spec
    state.data["proved"] = True
    state.data["answer"] = "Proof found: " + "; ".join(state.data.get("proof_steps", []))
    state.data["_done"] = True
    if state.data.get("_done"):
        return state

    return state


def process_emit_failure(state):
    """
    Emit Failure
    """
    print(f"  → Emit Failure")

    # Logic from spec
    state.data["proved"] = False
    state.data["answer"] = "Could not prove within iteration limit."
