def process_synthesize(state):
    """
    Synthesize
    Combine all agent results into the final deliverable
    """
    print(f"  → Synthesize")

    # Logic from spec
    agent_results = state.data.get("agent_results", {})
    state.data["all_results"] = list(agent_results.values())
    print(f"    Synthesizing {len(agent_results)} results into final output")
    if state.data.get("_done"):
        return state

    # Invoke: Combine results
    synthesizer_input = build_input(state, "SynthesisInput")
    synthesizer_msg = json.dumps(synthesizer_input, default=str)
    synthesizer_raw = invoke_synthesizer(synthesizer_msg, output_schema="SynthesisOutput")
    synthesizer_result = parse_response(synthesizer_raw, "SynthesisOutput")
    # Validate and merge output fields into state.data
    state.schema_violations += len(validate_output(synthesizer_result, "SynthesisOutput"))
    state.data.update(synthesizer_result)
    state.data["synthesis_output"] = synthesizer_result
    state.data["synthesize_result"] = synthesizer_result
    print(f"    ← Synthesizer: {synthesizer_result}")

    return state


def process_emit_output(state):
    """
    Emit Output
    Return the final deliverable
    """
    print(f"  → Emit Output")

    # Logic from spec
    output = state.data.get("final_output", state.data.get("synthesis", ""))
    print(f"    Final output: {str(output)[:200]}")
    state.data["_done"] = True
    if state.data.get("_done"):
        return state

    return state


