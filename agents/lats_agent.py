def process_initialize_search(state):
    """
    Initialize Search
    Setup the root node and search parameters
    """
    print(f"  → Initialize Search")

    # Logic from spec
    state.data["iteration"] = 0
    state.data["is_terminal"] = False
    state.data["best_score"] = 0.0
    state.data["tree"] = {"root": {"query": state.data.get("query"), "children": [], "visits": 0, "value": 0}}
    print(f"Starting LATS for: {state.data.get('query')}")
    if state.data.get("_done"):
        return state

    return state


def process_select_node(state):
    """
    Selection
    Select a leaf node to expand using UCT logic
    """
    print(f"  → Selection")

    # Read: Read Tree State
    tree_store_data = state.tree_store.read()
    state.data["tree_store"] = tree_store_data

    # Logic from spec
    # Logic to traverse the tree in state.data['tree'] using UCT
    # For this spec, we identify the 'current_trajectory' to expand
    state.data["current_trajectory"] = state.data.get("tree", {}).get("root", {}).get("query", "")
    print(f"Selecting node for iteration {state.data['iteration']}")
    if state.data.get("_done"):
        return state

    return state


def process_expand_node(state):
    """
    Expansion
    Generate candidate actions for the selected node
    """
    print(f"  → Expansion")

    # Logic from spec
    state.data["expansion_input"] = {
      "problem": state.data.get("query"),
      "trajectory": state.data.get("current_trajectory")
    }
    if state.data.get("_done"):
        return state

    # Flatten nested dicts: promote schema fields to top-level state.data
    for _nested_val in list(state.data.values()):
        if isinstance(_nested_val, dict):
            for _nk, _nv in _nested_val.items():
                if _nk not in state.data:
                    state.data[_nk] = _nv

    # Invoke: action_generator
    action_generator_input = build_input(state, "ExpansionInput")
    action_generator_msg = json.dumps(action_generator_input, default=str)
    action_generator_raw = invoke_action_generator(action_generator_msg, output_schema="ActionGeneratorOutput")
    action_generator_result = parse_response(action_generator_raw, "ActionGeneratorOutput")
    # Validate and merge output fields into state.data
    state.schema_violations += len(validate_output(action_generator_result, "ActionGeneratorOutput"))
    state.data.update(action_generator_result)
    state.data["action_generator_output"] = action_generator_result
    state.data["expand_node_result"] = action_generator_result
    print(f"    ← Action Generator: {action_generator_result}")

    return state


def process_evaluate_node(state):
    """
    Evaluation
    Evaluate the newly generated states
    """
    print(f"  → Evaluation")

    # Logic from spec
    # Prepare input for the evaluator based on the last action from action_generator
    actions = state.data.get("action_generator_output", {}).get("actions", [])
    state.data["evaluation_input"] = {
      "problem": state.data.get("query"),
      "trajectory": state.data.get("current_trajectory"),
      "new_state": actions[0] if actions else "No action"
    }
    if state.data.get("_done"):
        return state

    # Flatten nested dicts: promote schema fields to top-level state.data
    for _nested_val in list(state.data.values()):
        if isinstance(_nested_val, dict):
            for _nk, _nv in _nested_val.items():
                if _nk not in state.data:
                    state.data[_nk] = _nv

    # Invoke: state_evaluator
    state_evaluator_input = build_input(state, "EvaluationInput")
    state_evaluator_msg = json.dumps(state_evaluator_input, default=str)
    state_evaluator_raw = invoke_state_evaluator(state_evaluator_msg, output_schema="StateEvaluatorOutput")
    state_evaluator_result = parse_response(state_evaluator_raw, "StateEvaluatorOutput")
    # Validate and merge output fields into state.data
    state.schema_violations += len(validate_output(state_evaluator_result, "StateEvaluatorOutput"))
    state.data.update(state_evaluator_result)
    state.data["state_evaluator_output"] = state_evaluator_result
    state.data["evaluate_node_result"] = state_evaluator_result
    print(f"    ← State Evaluator: {state_evaluator_result}")

    return state


def process_backpropagate(state):
    """
    Backpropagation
    Update tree statistics with the evaluation result
    """
    print(f"  → Backpropagation")

    # Logic from spec
    eval_result = state.data.get("state_evaluator_output", {})
    score = eval_result.get("score", 0.0)
    state.data["is_terminal"] = eval_result.get("is_terminal", False)
    state.data["iteration"] += 1
    if score > state.data["best_score"]:
        state.data["best_score"] = score
        state.data["best_trajectory"] = state.data.get("current_trajectory")
    print(f"Iteration {state.data['iteration']} complete. Score: {score}")
    if state.data.get("_done"):
        return state

    # Write: Update Tree
    state.tree_store.write(state.data.copy())

    return state


def process_check_termination(state):
    """
    Search Finished?
    """
    print(f"  → Search Finished?")

    # Gate: is_terminal == True or iteration >= 30
    # Branch: continue → select_node
    # Branch: finish → format_output

    if (state.data.get("is_terminal") == True) or ((state.data.get("iteration", 0)) >= 30):
        print(f"    → continue")
        return "select_node"
    else:
        print(f"    → finish")
        return "format_output"


def process_format_output(state):
    """
    Format Output
    Prepare the final solution
    """
    print(f"  → Format Output")

    # Logic from spec
    state.data["formatter_input"] = {
      "problem": state.data.get("query"),
      "best_trajectory": state.data.get("best_trajectory", "No solution found")
    }
    if state.data.get("_done"):
        return state

    # Flatten nested dicts: promote schema fields to top-level state.data
    for _nested_val in list(state.data.values()):
        if isinstance(_nested_val, dict):
            for _nk, _nv in _nested_val.items():
                if _nk not in state.data:
                    state.data[_nk] = _nv

    # Invoke: solution_formatter
    solution_formatter_input = build_input(state, "FormatterInput")
    solution_formatter_msg = json.dumps(solution_formatter_input, default=str)
    solution_formatter_raw = invoke_solution_formatter(solution_formatter_msg, output_schema="FinalSolution")
    solution_formatter_result = parse_response(solution_formatter_raw, "FinalSolution")
    # Validate and merge output fields into state.data
    state.schema_violations += len(validate_output(solution_formatter_result, "FinalSolution"))
    state.data.update(solution_formatter_result)
    state.data["final_solution"] = solution_formatter_result
    state.data["format_output_result"] = solution_formatter_result
    print(f"    ← Solution Formatter: {solution_formatter_result}")

    return state


def process_finalize(state):
    """
    Finalize
    """
    print(f"  → Finalize")

    # Logic from spec
    state.data["_done"] = True
    print("LATS process complete.")
    if state.data.get("_done"):
        return state

    return state


