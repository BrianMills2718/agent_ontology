class AgentState:
    """Runtime state for Language Agent Tree Search (LATS)"""
    def __init__(self):
        self.tree_store = Store_tree_store()
        self.data = {}  # current data flowing through the pipeline
        self.iteration = 0
        self.schema_violations = 0


