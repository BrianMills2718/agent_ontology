class AgentState:
    """Runtime state for RAP (Reasoning via Planning)"""
    def __init__(self):
        self.search_tree = Store_search_tree()
        self.data = {}  # current data flowing through the pipeline
        self.iteration = 0
        self.schema_violations = 0


