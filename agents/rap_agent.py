def process_propose_actions(state):
    """
    Propose Actions
    """
    print(f"  → Propose Actions")

    # Invoke: action_proposer
    action_proposer_input = build_input(state, "ActionInput")
    action_proposer_msg = json.dumps(action_proposer_input, default=str)
    action_proposer_raw = invoke_action_proposer(action_proposer_msg, output_schema="ActionOutput")
    action_proposer_result = parse_response(action_proposer_raw, "ActionOutput")
    # Validate and merge output fields into state.data
    state.schema_violations += len(validate_output(action_proposer_result, "ActionOutput"))
    state.data.update(action_proposer_result)
    state.data["action_output"] = action_proposer_result
    state.data["propose_actions_result"] = action_proposer_result
    print(f"    ← Action Proposer: {action_proposer_result}")

    return state


def process_evaluate_state(state):
    """
    Evaluate State (World Model)
    """
    print(f"  → Evaluate State (World Model)")

    # Invoke: world_model_agent
    world_model_agent_input = build_input(state, "WorldModelInput")
    world_model_agent_msg = json.dumps(world_model_agent_input, default=str)
    world_model_agent_raw = invoke_world_model_agent(world_model_agent_msg, output_schema="WorldModelOutput")
    world_model_agent_result = parse_response(world_model_agent_raw, "WorldModelOutput")
    # Validate and merge output fields into state.data
    state.schema_violations += len(validate_output(world_model_agent_result, "WorldModelOutput"))
    state.data.update(world_model_agent_result)
    state.data["world_model_output"] = world_model_agent_result
    state.data["evaluate_state_result"] = world_model_agent_result
    print(f"    ← World Model (LLM): {world_model_agent_result}")

    return state


def process_check_done(state):
    """
    Search Complete?
    """
    print(f"  → Search Complete?")

    # Gate: iteration >= max_iterations
    # Branch: iteration >= max_iterations → emit_result
    # Branch: iteration < max_iterations → propose_actions

    if (state.data.get("iteration", 0)) >= (state.data.get("max_iterations", 0)):
        print(f"    → iteration >= max_iterations")
        return "emit_result"
    else:
        print(f"    → iteration < max_iterations")
        return "propose_actions"


def process_update_tree(state):
    """
    Update Search Tree
    """
    print(f"  → Update Search Tree")

    # Logic from spec
    state.data["iteration"] = state.data.get("iteration", 0) + 1
    reward = state.data.get("reward", 0)
    if reward > state.data.get("best_reward", 0):
        state.data["best_reward"] = reward
        state.data["best_path"] = state.data.get("current_path", [])
    if state.data.get("_done"):
        return state

    # Write: 
    state.search_tree.write(state.data.copy())

    return state


def process_emit_result(state):
    """
    Emit Best Result
    """
    print(f"  → Emit Best Result")

    # Logic from spec
    state.data["answer"] = state.data.get("current_state", "")
    state.data["reasoning_path"] = state.data.get("best_path", [])
    state.data["_done"] = True
    if state.data.get("_done"):
        return state

    return state


# ═══════════════════════════════════════════════════════════
# State Machine Executor
# ═══════════════════════════════════════════════════════════

PROCESSES = {
    "receive_problem": process_receive_problem,
    "mcts_search": process_mcts_search,
    "propose_actions": process_propose_actions,
    "evaluate_state": process_evaluate_state,
    "check_done": process_check_done,
    "update_tree": process_update_tree,
    "emit_result": process_emit_result,
}

TRANSITIONS = {
    "receive_problem": "propose_actions",
    "mcts_search": None,  # terminal
    "propose_actions": "evaluate_state",
    "evaluate_state": "update_tree",
    # "check_done": determined by gate logic
    "update_tree": "check_done",
    "emit_result": None,  # terminal
}


MAX_ITERATIONS = int(os.environ.get("AGENT_ONTOLOGY_MAX_ITER", "100"))


def run(initial_data=None):
    """RAP (Reasoning via Planning) — main execution loop"""
    state = AgentState()
    if initial_data:
        state.data.update(initial_data)

    current = "receive_problem"
    print(f"\n════════════════════════════════════════════════════════════")
    print(f"  RAP (Reasoning via Planning)")
    print(f"  RAP agent: LLM as world model + MCTS search for multi-step reasoning. The world model predicts state transitions; MCTS searches over the resulting tree to find optimal reasoning paths. Based on Hao et al. 2023.")
    print(f"════════════════════════════════════════════════════════════\n")

    while current and state.iteration < MAX_ITERATIONS:
        state.iteration += 1
        print(f"\n[Iteration {state.iteration}] State: {current}")

        process_fn = PROCESSES.get(current)
        if not process_fn:
            print(f"  Unknown process: {current}")
            break

        result = process_fn(state)

        if current in ['check_done']:
            current = result
        else:
            current = TRANSITIONS.get(current)

        # Fan-out: if transition is a list, run all branches sequentially
        while isinstance(current, list):
            _targets = current
            for _ft in _targets:
                state.iteration += 1
                print(f"\n[Iteration {state.iteration}] State: {_ft} (fan-out)")
                _fn = PROCESSES.get(_ft)
                if _fn:
                    _fn(state)
            # Collect unique next-hops from all fan-out targets
            _next_set = []
            for _ft in _targets:
                _nt = TRANSITIONS.get(_ft)
                if _nt is not None and _nt not in _next_set:
                    _next_set.append(_nt)
            current = _next_set[0] if len(_next_set) == 1 else (_next_set if _next_set else None)

        if current is None or state.data.get("_done"):
            print("\n  [DONE] Reached terminal state.")
            break

    _clean_exit = state.iteration < MAX_ITERATIONS
    if state.iteration >= MAX_ITERATIONS:
        print(f"\n  [STOPPED] Max iterations ({MAX_ITERATIONS}) reached.")
        _clean_exit = False

    if state.schema_violations > 0:
        print(f"\n  [SCHEMA] {state.schema_violations} total schema violation(s) during execution")
    dump_trace(iterations=state.iteration, clean_exit=_clean_exit)
    print(f"\nFinal state.data keys: {list(state.data.keys())}")
    return state


if __name__ == "__main__":
    run()