def process_check_quality(state):
    """
    Quality Check
    """
    print(f"  → Quality Check")

    # Gate: verdict == pass
    # Branch: verdict == pass → deliver
    # Branch: revision_count >= max_iterations → deliver
    # Branch: verdict == fail → implement_code

    if state.data.get("verdict") == "pass":
        return "deliver"
    elif (state.data.get("revision_count", 0)) >= (state.data.get("max_iterations", 0)):
        return "deliver"
    elif state.data.get("verdict") == "fail":
        return "implement_code"
    else:
        return "deliver"


def process_deliver(state):
    """
    Deliver
    """
