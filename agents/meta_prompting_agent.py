def process_receive_task(state):
    """
    Receive Task
    Accept user task and initialize meta-prompting state
    """
    print(f"  → Receive Task")

    # Logic from spec
    state.data["depth"] = 0
    state.data["max_depth"] = 3
    state.data["completed_sub_tasks"] = {}
    state.data["pending_sub_tasks"] = []
    state.data["generated_prompts"] = {}
    state.data["worker_results"] = []
    print(f"    Task: {state.data.get('task', '')[:100]}")
    if state.data.get("_done"):
        return state

    return state


def process_decompose_task(state):
    """
    Decompose Task
    Invoke the meta-agent to analyze and decompose the task into sub-tasks with generated prompts
    """
    print(f"  → Decompose Task")

    # Logic from spec
    depth = state.data.get("depth", 0)
    print(f"    Decomposing at depth {depth}...")
    if state.data.get("_done"):
        return state

    # Flatten nested dicts: promote schema fields to top-level state.data
    for _nested_val in list(state.data.values()):
        if isinstance(_nested_val, dict):
            for _nk, _nv in _nested_val.items():
                if _nk not in state.data:
                    state.data[_nk] = _nv

    # Invoke: Analyze and decompose task
    meta_agent_input = build_input(state, "MetaAgentInput")
    meta_agent_msg = json.dumps(meta_agent_input, default=str)
    meta_agent_raw = invoke_meta_agent(meta_agent_msg, output_schema="TaskDecomposition")
    meta_agent_result = parse_response(meta_agent_raw, "TaskDecomposition")
    # Validate and merge output fields into state.data
    state.schema_violations += len(validate_output(meta_agent_result, "TaskDecomposition"))
    state.data.update(meta_agent_result)
    state.data["task_decomposition"] = meta_agent_result
    state.data["decompose_task_result"] = meta_agent_result
    print(f"    ← Meta Agent: {meta_agent_result}")

    return state


def process_register_sub_tasks(state):
    """
    Register Sub-Tasks
    Store generated sub-tasks and their prompts, determine execution order
    """
    print(f"  → Register Sub-Tasks")

    # Logic from spec
    sub_tasks = state.data.get("sub_tasks", [])
    prompts = state.data.get("generated_prompts", {})
    for st in sub_tasks:
        st_id = st.get("sub_task_id", "")
        prompts[st_id] = st.get("generated_prompt", "")
    state.data["generated_prompts"] = prompts
    # Build execution queue respecting dependencies
    pending = [st for st in sub_tasks if st.get("sub_task_id") not in state.data.get("completed_sub_tasks", {})]
    state.data["pending_sub_tasks"] = pending
    state.data["current_sub_task_idx"] = 0
    print(f"    Registered {len(pending)} sub-tasks with generated prompts")
    if state.data.get("_done"):
        return state

    # Write: Store generated prompts
    prompt_library_write = build_input(state, "GeneratedPrompt")
    state.prompt_library.write(prompt_library_write)

    # Write: Register sub-tasks
    task_registry_write = build_input(state, "TaskRegistryEntry")
    state.task_registry.write(task_registry_write)

    return state


def process_check_pending(state):
    """
    Sub-tasks remaining?
    """
    print(f"  → Sub-tasks remaining?")

    # Gate: current_sub_task_idx < len(pending_sub_tasks)
    # Branch: sub-tasks remaining → prepare_worker
    # Branch: all sub-tasks done → integrate_results

    if (state.data.get("current_sub_task_idx", 0)) < (state.data.get("len", 0)):
        print(f"    → sub-tasks remaining")
        return "prepare_worker"
    else:
        print(f"    → all sub-tasks done")
        return "integrate_results"


def process_prepare_worker(state):
    """
    Prepare Worker
    Configure the next worker agent with the dynamically generated prompt
    """
    print(f"  → Prepare Worker")

    # Read: Read dependency results
    task_registry_data = state.task_registry.read()
    state.data["task_registry"] = task_registry_data

    # Logic from spec
    idx = state.data.get("current_sub_task_idx", 0)
    pending = state.data.get("pending_sub_tasks", [])
    if idx < len(pending):
        sub_task = pending[idx]
        state.data["current_sub_task"] = sub_task
        state.data["worker_prompt"] = sub_task.get("generated_prompt", "")
        state.data["worker_description"] = sub_task.get("description", "")
        state.data["worker_expertise"] = sub_task.get("required_expertise", "")
        state.data["is_complex"] = sub_task.get("is_complex", False)
        # Resolve dependencies
        depends = sub_task.get("depends_on", [])
        completed = state.data.get("completed_sub_tasks", {})
        state.data["dependency_context"] = [completed.get(d, "") for d in depends if d in completed]
        print(f"    Preparing worker for: {sub_task.get('sub_task_id', '')} ({sub_task.get('required_expertise', '')})")
    if state.data.get("_done"):
        return state

    return state


def process_check_complexity(state):
    """
    Needs recursive decomposition?
    """
    print(f"  → Needs recursive decomposition?")

    # Gate: is_complex == True and depth < max_depth
    # Branch: complex sub-task, recurse → recurse_decomposition
    # Branch: simple sub-task, execute → execute_worker

    if (state.data.get("depth", 0)) < (state.data.get("max_depth", 0)):
        print(f"    → complex sub-task, recurse")
        return "recurse_decomposition"
    else:
        print(f"    → simple sub-task, execute")
        return "execute_worker"


