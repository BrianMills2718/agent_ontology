class Store_pddl_store:
    """PDDL Domain Store (pddl_domain)"""
    def __init__(self):
        self.data = {}
        self._rules = []

    def read(self, key=None):
        if key is None:
            return self._rules
        return [r for r in self._rules if key in str(r)]

    def write(self, value, key=None):
        self._rules.append(value)



