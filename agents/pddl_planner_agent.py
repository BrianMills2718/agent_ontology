def process_invoke_translator(state):
    """
    Invoke Translator Agent
    """
    print(f"  → Invoke Translator Agent")

    # Invoke: translator
    translator_input = build_input(state, "TranslationInput")
    translator_msg = json.dumps(translator_input, default=str)
    translator_raw = invoke_translator(translator_msg, output_schema="PDDLOutput")
    translator_result = parse_response(translator_raw, "PDDLOutput")
    # Validate and merge output fields into state.data
    state.schema_violations += len(validate_output(translator_result, "PDDLOutput"))
    state.data.update(translator_result)
    state.data["pddl_output"] = translator_result
    state.data["invoke_translator_result"] = translator_result
    print(f"    ← NL-PDDL Translator: {translator_result}")

    # Write: 
    state.pddl_store.write(state.data.copy())

    return state


