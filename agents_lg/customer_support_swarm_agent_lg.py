def route_route(state: AgentState) -> str:
    """Gate: Route to Specialist — intent classification"""
    if state.get("intent") == "billing":
        return "handle_billing"
    elif state.get("intent") == "technical":
        return "handle_tech"
    elif state.get("intent") == "account":
        return "handle_account"
    else:
        return "respond_general"


# ═══════════════════════════════════════════════════════════
# Graph Construction
# ═══════════════════════════════════════════════════════════

def build_graph():
    graph = StateGraph(AgentState)

    graph.add_node("intake", node_intake)
    graph.add_node("triage", node_triage)
    graph.add_node("handle_billing", node_handle_billing)
    graph.add_node("handle_tech", node_handle_tech)
    graph.add_node("handle_account", node_handle_account)
    graph.add_node("respond_general", node_respond_general)
    graph.add_node("finalize", node_finalize)

    graph.set_entry_point("intake")

    graph.add_edge("intake", "triage")
    graph.add_conditional_edges(
        "triage",
        route_route,
        {
            "handle_billing": "handle_billing",
            "handle_tech": "handle_tech",
            "handle_account": "handle_account",
            "respond_general": "respond_general",
        }
    )
    graph.add_edge("handle_billing", "finalize")
    graph.add_edge("handle_tech", "finalize")
    graph.add_edge("handle_account", "finalize")
    graph.add_edge("respond_general", "finalize")
    graph.add_edge("finalize", END)

    return graph.compile()


