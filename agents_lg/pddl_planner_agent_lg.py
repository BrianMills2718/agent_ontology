# Graph Construction
# ═══════════════════════════════════════════════════════════

def build_graph():
    graph = StateGraph(AgentState)

    graph.add_node("receive_task", node_receive_task)
    graph.add_node("translate_to_pddl", node_translate_to_pddl)
    graph.add_node("invoke_translator", node_invoke_translator)
    graph.add_node("run_planner", node_run_planner)
    graph.add_node("translate_to_nl", node_translate_to_nl)
    graph.add_node("invoke_explainer", node_invoke_explainer)
    graph.add_node("emit_result", node_emit_result)
    graph.add_node("handle_unsolvable", node_handle_unsolvable)

    graph.set_entry_point("receive_task")

    graph.add_edge("receive_task", "translate_to_pddl")
    graph.add_edge("translate_to_pddl", "invoke_translator")
    graph.add_edge("invoke_translator", "run_planner")
    graph.add_conditional_edges(
        "run_planner",
        route_check_solvable,
        {
            "translate_to_nl": "translate_to_nl",
            "handle_unsolvable": "handle_unsolvable",
        }
    )
    graph.add_edge("translate_to_nl", "invoke_explainer")
    graph.add_edge("invoke_explainer", "emit_result")
    graph.add_edge("emit_result", END)
    graph.add_edge("handle_unsolvable", END)

    return graph.compile()


