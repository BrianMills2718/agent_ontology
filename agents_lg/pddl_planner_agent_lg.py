def invoke_translator(user_message, output_schema=None):
    """NL-PDDL Translator"""
    system = """You translate natural language task descriptions into PDDL problem definitions. Given a domain description and a task, produce a valid PDDL problem file. Output only the PDDL, no explanation.
"""
    if output_schema:
        system += output_instruction(output_schema)
    t0 = time.time()
    result = call_llm(
        model="gemini-3-flash-preview",
        system_prompt=system,
        user_message=user_message,
        temperature=0.7,
        max_tokens=4096,
    )
    trace_call("NL-PDDL Translator", "gemini-3-flash-preview", system, user_message, result, int((time.time()-t0)*1000))
    return result


def invoke_explainer(user_message, output_schema=None):
    """Plan Explainer"""
    system = """You translate formal plans into clear natural language explanations. Given a sequence of PDDL actions, explain each step in plain English.
"""
    if output_schema:
        system += output_instruction(output_schema)
    t0 = time.time()
    result = call_llm(
        model="gemini-3-flash-preview",
        system_prompt=system,
        user_message=user_message,
        temperature=0.7,
        max_tokens=4096,
    )
    trace_call("Plan Explainer", "gemini-3-flash-preview", system, user_message, result, int((time.time()-t0)*1000))
    return result



