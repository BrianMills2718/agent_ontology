def node_shuffle_results(state: AgentState) -> dict:
    """
    Shuffle and Group
    Group and deduplicate findings from parallel map tasks.
    """
    print(f"  → Shuffle and Group")
    updates = {}

    # Logic from spec
    # Collect results from the 'map_chunks' spawn process
    # Results are typically found in state.get('chunk_result', "") as a list
    raw_results = state.get("chunk_result", [])
    
    # Deduplication logic based on content similarity or offsets
    deduplicated = []
    seen_findings = set()
    for res in raw_results:
        for finding in res.get("findings", []):
            if finding not in seen_findings:
                deduplicated.append(finding)
                seen_findings.add(finding)
    
    updates["deduplicated_results"] = deduplicated
    print(f"Shuffle complete. {len(deduplicated)} unique findings identified.")
    if updates.get("_done") or state.get("_done"):
        return updates

    return updates


def node_reduce_results(state: AgentState) -> dict:
    """
    Reduce Results
    Prepare deduplicated data and invoke the Reduce Agent for synthesis.
    """
    print(f"  → Reduce Results")
    updates = {}

    # Logic from spec
    updates["retry_count"] = state.get("retry_count", 0) + 1
    # Prepare the input for the reduce agent
    updates["reduce_input"] = {
        "task_description": state.get("task_description"),
        "findings": state.get("deduplicated_results", [])
    }
    if updates.get("_done") or state.get("_done"):
        return updates

    # Flatten nested dicts
    _combined = dict(state)
    _combined.update(updates)
    for _nested_val in list(_combined.values()):
        if isinstance(_nested_val, dict):
            for _nk, _nv in _nested_val.items():
                if _nk not in _combined:
                    updates[_nk] = _nv

    # Invoke: Synthesize Findings
    _cur = dict(state)
    _cur.update(updates)
    reduce_agent_input = build_input(_cur, "ReduceTaskInput")
    reduce_agent_msg = json.dumps(reduce_agent_input, default=str)
    reduce_agent_raw = invoke_reduce_agent(reduce_agent_msg, output_schema="ReduceOutputSchema")
    reduce_agent_result = parse_response(reduce_agent_raw, "ReduceOutputSchema")
    updates["_schema_violations"] = len(validate_output(reduce_agent_result, "ReduceOutputSchema"))
    updates.update(reduce_agent_result)
    updates["reduce_output_schema"] = reduce_agent_result
    updates["reduce_results_result"] = reduce_agent_result
    print(f"    ← Reduce Agent: {reduce_agent_result}")

    return updates


