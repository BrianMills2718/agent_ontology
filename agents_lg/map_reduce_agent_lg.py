def node_chunk_document(state: AgentState) -> dict:
    """
    Chunk Document
    Split the input document into overlapping chunks based on token size.
    """
    print(f"  → Chunk Document")
    updates = {}

    # Logic from spec
    text = state.get("full_text", "")
    chunk_size = state.get("chunk_size", 1000)
    overlap = int(chunk_size * 0.1)
    
    # Mock chunking logic
    chunks = []
    words = text.split()
    for i in range(0, len(words), chunk_size - overlap):
        chunk_text = " ".join(words[i:i + chunk_size])
        chunks.append({
            "chunk_id": f"chunk_{len(chunks)}",
            "content": chunk_text,
            "start_offset": i,
            "end_offset": i + len(chunk_text)
        })
    
    updates["chunks"] = chunks
    updates["chunk_count"] = len(chunks)
    updates["retry_count"] = 0
    print(f"Document split into {len(chunks)} chunks.")
    if updates.get("_done") or state.get("_done"):
        return updates

    return updates


