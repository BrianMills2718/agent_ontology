def invoke_map_agent(user_message, output_schema=None):
    """Map Agent"""
    system = """You are a specialized analysis agent. You will receive a document chunk and a specific analysis task.
Your goal is to extract all relevant information from this chunk according to the task.
Provide a relevance score (0-1) for how much useful information was found in this specific chunk.
Include metadata about the chunk if relevant.
"""
    if output_schema:
        system += output_instruction(output_schema)
    t0 = time.time()
    result = call_llm(
        model="gemini-3-flash-preview",
        system_prompt=system,
        user_message=user_message,
        temperature=0.7,
        max_tokens=4096,
    )
    trace_call("Map Agent", "gemini-3-flash-preview", system, user_message, result, int((time.time()-t0)*1000))
    return result


def invoke_reduce_agent(user_message, output_schema=None):
    """Reduce Agent"""
    system = """You are a synthesis agent. You will receive a list of findings extracted from different parts of a large document.
Your task is to reconcile conflicting information, deduplicate overlapping findings, and synthesize a single coherent final report.
Establish cross-chunk references where necessary.
Provide a confidence score (0-1) for the final synthesis.
If the input data is insufficient or contradictory, lower the confidence score.
"""
    if output_schema:
        system += output_instruction(output_schema)
    t0 = time.time()
    result = call_llm(
        model="gemini-3-flash-preview",
        system_prompt=system,
        user_message=user_message,
        temperature=0.7,
        max_tokens=4096,
    )
    trace_call("Reduce Agent", "gemini-3-flash-preview", system, user_message, result, int((time.time()-t0)*1000))
    return result



