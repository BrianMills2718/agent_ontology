def build_graph():
    graph = StateGraph(AgentState)

    graph.add_node("chunk_document", node_chunk_document)
    graph.add_node("map_chunks", node_map_chunks)
    graph.add_node("shuffle_results", node_shuffle_results)
    graph.add_node("reduce_results", node_reduce_results)
    graph.add_node("finalize_output", node_finalize_output)

    graph.set_entry_point("chunk_document")

    graph.add_edge("chunk_document", "map_chunks")
    graph.add_edge("map_chunks", "shuffle_results")
    graph.add_edge("shuffle_results", "reduce_results")
    graph.add_conditional_edges(
        "reduce_results",
        route_check_quality,
        {
            "finalize_output": "finalize_output",
            "reduce_results": "reduce_results",
        }
    )
    graph.add_edge("finalize_output", END)

    return graph.compile()


