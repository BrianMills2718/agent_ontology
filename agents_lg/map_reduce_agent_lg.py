def route_check_quality(state: AgentState) -> str:
    """Gate: Check Quality — confidence_score >= 0.7"""
    if (state.get("confidence_score", 0)) >= 0.7:
        print(f"    → confidence >= 0.7 or retries exhausted")
        return "finalize_output"
    else:
        print(f"    → confidence < 0.7 and retries < 2")
        return "reduce_results"


