def node_reanalyze(state: AgentState) -> dict:
    """
    Re-analyze Mutated Spec
    Run analysis on the mutated spec to compare with original
    """
    print(f"  → Re-analyze Mutated Spec")
    updates = {}

    # Logic from spec
    import yaml as _yaml
    import tempfile, os
    mutated_yaml = state.get("mutated_spec_yaml", "")
    tmp = tempfile.NamedTemporaryFile(mode='w', suffix='.yaml', delete=False)
    tmp.write(mutated_yaml)
    tmp.close()
    try:
        spec = _yaml.safe_load(mutated_yaml)
        # Run lint on mutated spec
        try:
            from agent_ontology.lint import lint_spec
            new_lint = lint_spec(spec)
            updates["new_lint_warnings"] = [{"rule": r.code, "severity": r.severity, "message": r.message} for r in new_lint]
            print(f"    New lint: {len(new_lint)} warnings (was {len(state.get('lint_warnings', []))})")
        except Exception:
            updates["new_lint_warnings"] = []
        # Run verify on mutated spec
        try:
            from agent_ontology.verify import verify_spec
            passes, failures = verify_spec(spec, spec_path=tmp.name)
            updates["new_verify_issues"] = [{"check": f, "status": "fail"} for f in failures]
        except Exception:
            updates["new_verify_issues"] = []
        updates["new_entity_count"] = len(spec.get("entities", []))
        updates["new_process_count"] = len(spec.get("processes", []))
        updates["new_edge_count"] = len(spec.get("edges", []))
    finally:
        os.unlink(tmp.name)
    if updates.get("_done") or state.get("_done"):
        return updates

    return updates


def node_evaluate(state: AgentState) -> dict:
    """
    Evaluate Improvement
    Compare original and mutated analyses to judge improvement
    """
    print(f"  → Evaluate Improvement")
    updates = {}

    # Logic from spec
    print(f"    Evaluating improvement quality...")
    if updates.get("_done") or state.get("_done"):
        return updates

    # Flatten nested dicts
    _combined = dict(state)
    _combined.update(updates)
    for _nested_val in list(_combined.values()):
        if isinstance(_nested_val, dict):
            for _nk, _nv in _nested_val.items():
                if _nk not in _combined:
                    updates[_nk] = _nv

    # Invoke: Evaluate improvement
    _cur = dict(state)
    _cur.update(updates)
    evaluator_input = build_input(_cur, "EvaluatorInput")
    evaluator_msg = json.dumps(evaluator_input, default=str)
    evaluator_raw = invoke_evaluator(evaluator_msg, output_schema="EvaluatorOutput")
    evaluator_result = parse_response(evaluator_raw, "EvaluatorOutput")
    updates["_schema_violations"] = len(validate_output(evaluator_result, "EvaluatorOutput"))
    updates.update(evaluator_result)
    updates["evaluator_output"] = evaluator_result
    updates["evaluate_result"] = evaluator_result
    print(f"    ← Improvement Evaluator: {evaluator_result}")

    return updates


def node_accept_improvement(state: AgentState) -> dict:
    """
    Accept Improvement
    Apply the accepted mutation as the new current spec
    """
    print(f"  → Accept Improvement")
    updates = {}

    # Logic from spec
    updates["current_spec_yaml"] = state.get("mutated_spec_yaml", state.get("current_spec_yaml", ""))
    improvement = {
        "round": state.get("improvement_round", 0) + 1,
        "type": state.get("improvement_type", ""),
        "details": state.get("improvement_details", ""),
        "score_delta": state.get("score_delta", 0),
    }
    improvements = state.get("improvements_made", [])
    improvements.append(improvement)
    updates["improvements_made"] = improvements
    updates["improvement_round"] = state.get("improvement_round", 0) + 1
    print(f"    Accepted: {improvement['type']} (delta: {improvement['score_delta']:+d})")
    if updates.get("_done") or state.get("_done"):
        return updates

    return updates


