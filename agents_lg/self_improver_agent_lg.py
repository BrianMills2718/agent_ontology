def route_check_needs_improvement(state: AgentState) -> str:
    """Gate: Needs improvement? — improvement_type is not empty"""
    if bool(state.get("improvement_type")):
        print(f"    → has improvement to make")
        return "mutate_spec"
    else:
        print(f"    → no improvement needed")
        return "finalize"


def route_check_valid(state: AgentState) -> str:
    """Gate: Mutation valid? — mutation_valid == True"""
    if state.get("mutation_valid") == True:
        print(f"    → valid mutation")
        return "reanalyze"
    else:
        print(f"    → invalid mutation")
        return "handle_invalid"


def route_check_improved(state: AgentState) -> str:
    """Gate: Improvement accepted? — improved == True"""
    if state.get("improved") == True:
        print(f"    → improvement accepted")
        return "accept_improvement"
    else:
        print(f"    → improvement rejected")
        return "reject_improvement"


def route_check_rounds_remaining(state: AgentState) -> str:
    """Gate: More rounds? — improvement_round < max_rounds"""
    if (state.get("improvement_round", 0)) < (state.get("max_rounds", 0)):
        print(f"    → rounds remaining")
        return "analyze_spec"
    else:
        print(f"    → max rounds reached")
        return "finalize"


