def build_graph():
    graph = StateGraph(AgentState)

    graph.add_node("receive_spec", node_receive_spec)
    graph.add_node("analyze_spec", node_analyze_spec)
    graph.add_node("diagnose", node_diagnose)
    graph.add_node("mutate_spec", node_mutate_spec)
    graph.add_node("extract_mutated_yaml", node_extract_mutated_yaml)
    graph.add_node("validate_mutation", node_validate_mutation)
    graph.add_node("handle_invalid", node_handle_invalid)
    graph.add_node("reanalyze", node_reanalyze)
    graph.add_node("evaluate", node_evaluate)
    graph.add_node("accept_improvement", node_accept_improvement)
    graph.add_node("reject_improvement", node_reject_improvement)
    graph.add_node("finalize", node_finalize)

    graph.set_entry_point("receive_spec")

    graph.add_edge("receive_spec", "analyze_spec")
    graph.add_edge("analyze_spec", "diagnose")
    graph.add_conditional_edges(
        "diagnose",
        route_check_needs_improvement,
        {
            "mutate_spec": "mutate_spec",
            "finalize": "finalize",
        }
    )
    graph.add_edge("mutate_spec", "extract_mutated_yaml")
    graph.add_edge("extract_mutated_yaml", "validate_mutation")
    graph.add_conditional_edges(
        "validate_mutation",
        route_check_valid,
        {
            "reanalyze": "reanalyze",
            "handle_invalid": "handle_invalid",
        }
    )
    graph.add_edge("handle_invalid", "finalize")
    graph.add_edge("reanalyze", "evaluate")
    graph.add_conditional_edges(
        "evaluate",
        route_check_improved,
        {
            "accept_improvement": "accept_improvement",
            "reject_improvement": "reject_improvement",
        }
    )
    graph.add_conditional_edges(
        "accept_improvement",
        route_check_rounds_remaining,
        {
            "analyze_spec": "analyze_spec",
            "finalize": "finalize",
        }
    )
    graph.add_conditional_edges(
        "reject_improvement",
        route_check_rounds_remaining,
        {
            "analyze_spec": "analyze_spec",
            "finalize": "finalize",
        }
    )
    graph.add_edge("finalize", END)

    return graph.compile()


