def node_receive_problem(state: AgentState) -> dict:
    """
    Receive Problem
    Accept the problem input
    """
    print(f"  → Receive Problem")
    updates = {}

    # Logic from spec
    print(f"    Problem: {state.get('problem', '')[:100]}")
    if updates.get("_done") or state.get("_done"):
        return updates

    return updates


def node_solve(state: AgentState) -> dict:
    """
    Solve Problem
    Invoke the solver agent to produce an answer
    """
    print(f"  → Solve Problem")
    updates = {}

    # Logic from spec
    print(f"    Solving...")
    if updates.get("_done") or state.get("_done"):
        return updates

    # Flatten nested dicts
    _combined = dict(state)
    _combined.update(updates)
    for _nested_val in list(_combined.values()):
        if isinstance(_nested_val, dict):
            for _nk, _nv in _nested_val.items():
                if _nk not in _combined:
                    updates[_nk] = _nv

    # Invoke: Invoke solver
    _cur = dict(state)
    _cur.update(updates)
    solver_input = build_input(_cur, "ProblemInput")
    solver_msg = json.dumps(solver_input, default=str)
    solver_raw = invoke_solver(solver_msg, output_schema="SolverOutput")
    solver_result = parse_response(solver_raw, "SolverOutput")
    updates["_schema_violations"] = len(validate_output(solver_result, "SolverOutput"))
    updates.update(solver_result)
    updates["solver_output"] = solver_result
    updates["solve_result"] = solver_result
    print(f"    ← Solver Agent: {solver_result}")

    return updates


