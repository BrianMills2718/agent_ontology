class AgentState(TypedDict, total=False):
    """Typed state for Minimal Solver"""
    _canned_responses: list
    _done: bool
    _iteration: int
    _schema_violations: Annotated[int, operator.add]
    answer: str
    problem: str
    solve_result: Any


